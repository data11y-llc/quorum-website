function colorDistanceSquared(
  red: number,
  green: number,
  blue: number,
  redTrue: number,
  greenTrue: number,
  blueTrue: number
): number {
  const redDistance = Math.pow(red - redTrue, 2);
  const greenDistance = Math.pow(green - greenTrue, 2);
  const blueDistance = Math.pow(blue - blueTrue, 2);
  return redDistance + greenDistance + blueDistance;
}

export function convertRGBValuesToEnglishNames(red: number, green: number, blue: number): string {
  let nearestColor = 'white';
  let nearestDistanceSquared = Number.MAX_SAFE_INTEGER;

  for (let i = 0; i < colorObject.length; i++) {
    const currentDistanceSquared = colorDistanceSquared(
      red,
      green,
      blue,
      colorObject[i].r,
      colorObject[i].g,
      colorObject[i].b
    );

    if (currentDistanceSquared < nearestDistanceSquared) {
      nearestDistanceSquared = currentDistanceSquared;
      nearestColor = colorObject[i].colorName;
    }
  }

  return nearestColor;
}

